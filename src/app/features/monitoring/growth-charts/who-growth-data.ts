// ── UTILITAIRE : calcul du percentile patient ─────────────────────────────────

/**
 * Interpole linéairement la valeur patient entre deux points OMS
 * pour estimer son percentile.
 */
export function calculatePercentile(
  ageMonths: number,
  value: number,
  chartType: string,
  gender: GenderType
): number | null {
  const rows = WHO_DATA[chartType]?.[gender];
  if (!rows) return null;

  // Trouver les 2 lignes OMS encadrant l'âge
  let lower = rows[0];
  let upper = rows[rows.length - 1];

  for (let i = 0; i < rows.length - 1; i++) {
    if (rows[i].age <= ageMonths && rows[i + 1].age >= ageMonths) {
      lower = rows[i];
      upper = rows[i + 1];
      break;
    }
  }

  // Interpolation linéaire entre les deux lignes
  const ratio = upper.age === lower.age ? 0
    : (ageMonths - lower.age) / (upper.age - lower.age);

  const interp = (a: number, b: number) => a + ratio * (b - a);

  const p3  = interp(lower.p3,  upper.p3);
  const p15 = interp(lower.p15, upper.p15);
  const p50 = interp(lower.p50, upper.p50);
  const p85 = interp(lower.p85, upper.p85);
  const p97 = interp(lower.p97, upper.p97);

  // Estimation du percentile par interpolation entre les seuils connus
  if (value <= p3)  return Math.max(0, 3 * (value / p3));
  if (value <= p15) return 3  + 12  * ((value - p3)  / (p15 - p3));
  if (value <= p50) return 15 + 35  * ((value - p15) / (p50 - p15));
  if (value <= p85) return 50 + 35  * ((value - p50) / (p85 - p50));
  if (value <= p97) return 85 + 12  * ((value - p85) / (p97 - p85));
  return Math.min(100, 97 + 3 * ((value - p97) / (p97 * 0.05)));
}